package SnakeAndLadder.Models;

public class Jumper {
    private int start;
    private int end;

    public Jumper(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }
     
    public int getEnd() {
        return end;
    }
}
